//bs1
const bsArticle1 = document.querySelector(".bs-article1");
const bsArticleImg1 = document.querySelector(".bs-article1 a img");
bsArticle1.addEventListener(
  "mouseover",
  function (event) {
    bsArticleImg1.classList.add("bs-article-img-hover");
    
  },
  false
);
bsArticle1.addEventListener(
  "mouseout",
  function (event) {
    bsArticleImg1.classList.remove("bs-article-img-hover");
    
  },
  false
);

